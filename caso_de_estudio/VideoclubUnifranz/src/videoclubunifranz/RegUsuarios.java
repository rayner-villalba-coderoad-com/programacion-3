    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombreUsuario = this.nombre.getText();
        String usuario = this.usuario.getText();
        String pass = new String(this.password.getPassword());
        int numeroDeRegistros = 0;
        try {
            //Paso 2 conectarse a la BD
            manejadorBD.conectarseBD();
           
            
            ResultSet registro = manejadorBD.consultarDB("select count(*) from empleado");
            
            while (registro.next()) {
                numeroDeRegistros = registro.getInt("count(*)");
                System.out.println("Cantidad registros: "  + numeroDeRegistros);
            }
            
            numeroDeRegistros = numeroDeRegistros + 1;
            
            //Paso 3 ejecuto el SQL
            manejadorBD.insertarDB("insert into empleado(idempleado,nombre,usuario,password) values (" + numeroDeRegistros + ", '"+ nombreUsuario + "', '"+ usuario+"', '"+ pass + "')");
           
            //Paso 4 Cerrar conexion 
            manejadorBD.cerrarConexionBD();
            
            JOptionPane.showMessageDialog(null, "Se ha creado un usuario nuevo ", "Creacion de Usuario", JOptionPane.OK_OPTION);
            
            //Limpiar Campos
           // this.nombre.setText("");
           // this.usuario.setText("");
           // this.password.setText("");
            
            //Ocultar la ventana
            this.setVisible(false);

        }catch (Exception ex) {
            System.out.println("Error: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

