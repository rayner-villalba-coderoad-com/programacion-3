    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombreUsuario = this.buscarNombre.getText().trim();
        System.out.println("Nombre de: '" + nombreUsuario + "'");
        
        if (nombreUsuario.length()==0 || nombreUsuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese el nombre del usuario a buscar", "Test", JOptionPane.INFORMATION_MESSAGE);
        } else {
             try {
                //Consultar a la Base de datos
                manejadorBD.conectarseBD();
                //Ejecutar la transaccion
                ResultSet registro = manejadorBD.consultarDB("select * from empleado where nombre='"+ nombreUsuario + "'");
            
                if(registro.next() == true) {
                    this.nombre.setText(registro.getString("nombre"));
                    this.usuario.setText(registro.getString("usuario"));
                    this.password.setText(registro.getString("password"));
                    this.idEmpleado = registro.getInt("idempleado");
                    
                    this.PanelResultado.setVisible(true);
                } else {
                    this.idEmpleado = 0;
                    JOptionPane.showMessageDialog(this, "No se han encontrado al usuario " + nombreUsuario, "", JOptionPane.INFORMATION_MESSAGE);
                }
                
                
                manejadorBD.cerrarConexionBD();
                
            } catch (SQLException ex) {
                Logger.getLogger(EdicionUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nombreUsuario = this.nombre.getText();
        String usu = this.usuario.getText();
        String pass = new String(this.password.getPassword());
        
         try {
                //Consultar a la Base de datos
                manejadorBD.conectarseBD();
                //Ejecutar la transaccion
                manejadorBD.insertarDB("UPDATE empleado SET nombre='"+ nombreUsuario + "', usuario='" + usu + "', password='" + pass+ "' where idempleado='" + this.idEmpleado + "'");
            
               
                manejadorBD.cerrarConexionBD();
                
                JOptionPane.showMessageDialog(this,"Los Datos del usuario " + nombreUsuario + " han sido actualizado correctamente!!", "Actualizacion->Usuario", JOptionPane.OK_OPTION);
                this.nombre.setText("");
                this.usuario.setText("");
                this.password.setText("");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(EdicionUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

